    // ---- PR-038: compiler determinism, alias resolution, context packs ----

    fn token(path: &str, ty: &str, value: serde_json::Value) -> DesignToken {
        DesignToken {
            path: path.to_string(),
            token_type: ty.to_string(),
            value,
            unit: if ty == "dimension" {
                Some("pt".to_string())
            } else {
                None
            },
            semantic_role: None,
            confidence: None,
            source_refs: vec![],
            contrast_constraints: vec![],
        }
    }

    fn set_with(tokens: Vec<DesignToken>) -> DesignTokenSet {
        DesignTokenSet {
            schema: DESIGN_TOKEN_SET_SCHEMA.to_string(),
            design_system_id: "demo".to_string(),
            revision: 1,
            tokens,
        }
    }

    fn bundled_set() -> DesignTokenSet {
        serde_json::from_str(TOKENS_JSON).expect("bundled tokens parse")
    }

    #[test]
    fn swift_and_css_compilation_is_byte_identical_across_runs() {
        // DETERMINISM: compiling the bundled sample token set twice yields
        // identical Swift and identical CSS bytes (no HashMap order, no clock).
        let set = bundled_set();
        let resolved = resolve_token_set(&set).expect("bundled set resolves");

        let swift_a = compile_swift_tokens(&set);
        let swift_b = compile_swift_tokens(&set);
        assert_eq!(swift_a, swift_b, "Swift output is not deterministic");

        let css_a = compile_css_tokens(&resolved);
        let css_b = compile_css_tokens(&resolved);
        assert_eq!(css_a, css_b, "CSS output is not deterministic");

        // The CSS adapter sorts custom properties by token path.
        assert!(css_a.contains("--color-accent-primary: #5EDEC4;"));
        assert!(css_a.contains("--size-hit-target-primary: 44px;"));
        let accent = css_a.find("--color-accent-primary").unwrap();
        let canvas = css_a.find("--color-canvas").unwrap();
        assert!(accent < canvas, "CSS properties must be sorted by path");
    }

    #[test]
    fn alias_chain_resolves_to_concrete_value() {
        // a -> b -> #5EDEC4 resolves both `a` and `b` to the concrete hex.
        let set = set_with(vec![
            token("color.a", "color", serde_json::json!("{color.b}")),
            token("color.b", "color", serde_json::json!("{color.brand}")),
            token("color.brand", "color", serde_json::json!("#5EDEC4")),
        ]);
        let resolved = resolve_token_set(&set).expect("alias chain resolves");
        assert_eq!(
            resolved.get("color.a").unwrap().value,
            serde_json::json!("#5EDEC4")
        );
        assert_eq!(
            resolved.get("color.b").unwrap().value,
            serde_json::json!("#5EDEC4")
        );
    }

    #[test]
    fn cyclic_alias_is_rejected() {
        // a -> b -> a is a cycle.
        let set = set_with(vec![
            token("color.a", "color", serde_json::json!("{color.b}")),
            token("color.b", "color", serde_json::json!("{color.a}")),
        ]);
        let error = resolve_token_set(&set).expect_err("cycle rejected");
        assert_eq!(error.reason_code(), "design_alias_cycle");
        assert!(matches!(error, DesignCompileError::AliasCycle { .. }));
    }

    #[test]
    fn unresolved_alias_is_rejected() {
        let set = set_with(vec![token(
            "color.a",
            "color",
            serde_json::json!("{color.missing}"),
        )]);
        let error = resolve_token_set(&set).expect_err("unresolved rejected");
        assert_eq!(error.reason_code(), "design_alias_unresolved");
    }

    #[test]
    fn type_mismatched_alias_is_rejected() {
        // A color token aliasing a dimension/number is a type error.
        let set = set_with(vec![
            token("color.a", "color", serde_json::json!("{size.gap}")),
            token("size.gap", "dimension", serde_json::json!(8)),
        ]);
        let error = resolve_token_set(&set).expect_err("type mismatch rejected");
        assert_eq!(error.reason_code(), "design_alias_type_mismatch");
        assert!(matches!(
            error,
            DesignCompileError::AliasTypeMismatch { .. }
        ));
    }

