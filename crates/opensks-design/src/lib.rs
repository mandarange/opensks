    #[test]
    fn context_pack_selects_only_relevant_within_budget_and_is_deterministic() {
        // A set with tokens relevant to "color" and irrelevant ones (spacing).
        let set = bundled_set();
        let resolved = resolve_token_set(&set).expect("resolves");
        let components = DesignPackageComponents {
            schema: "opensks.component-catalog.v1".to_string(),
            design_system_id: "demo".to_string(),
            components: vec![],
        };
        let context = compiler::DesignContext::new(&resolved).with_components(&components);

        let pack = build_design_context_pack(&context, "color accent", 3, 100_000);
        // Only color tokens selected — no dimension/spacing tokens.
        assert!(!pack.items.is_empty());
        assert!(
            pack.items
                .iter()
                .all(|item| item.reference.starts_with("color.")),
            "irrelevant (non-color) items must be excluded"
        );
        // Item budget respected.
        assert!(pack.items.len() <= 3);
        // Char budget respected.
        assert!(pack.total_chars <= 100_000);
        // Body matches the selected item texts joined by newline.
        let expected_body = pack
            .items
            .iter()
            .map(|i| i.text.clone())
            .collect::<Vec<_>>()
            .join("\n");
        assert_eq!(pack.body, expected_body);

        // Same query + budget -> identical pack (byte-identical JSON).
        let pack_again = build_design_context_pack(&context, "color accent", 3, 100_000);
        assert_eq!(pack, pack_again);
        let json_a = serde_json::to_string(&pack).unwrap();
        let json_b = serde_json::to_string(&pack_again).unwrap();
        assert_eq!(json_a, json_b);
    }

    #[test]
    fn context_pack_char_budget_excludes_over_budget_items() {
        let set = bundled_set();
        let resolved = resolve_token_set(&set).expect("resolves");
        let context = compiler::DesignContext::new(&resolved);
        // A tiny char budget admits at most one short line.
        let pack = build_design_context_pack(&context, "color", 50, 40);
        assert!(pack.total_chars <= 40);
        // Every admitted item individually fit.
        assert!(pack.items.iter().all(|item| item.char_cost <= 40));
    }

    #[test]
    fn context_pin_is_stable_and_verifiable() {
        let set = bundled_set();
        let resolved = resolve_token_set(&set).expect("resolves");
        let context = compiler::DesignContext::new(&resolved);
        let pack = build_design_context_pack(&context, "color", 3, 100_000);

        let pin_a = pin_design_context(&pack, "model-x", "rev-7");
        let pin_b = pin_design_context(&pack, "model-x", "rev-7");
        assert_eq!(pin_a, pin_b, "pin must be deterministic");
        assert!(verify_design_context_pin(&pin_a, &pack, "model-x", "rev-7"));
        // A different model id or revision fails verification.
        assert!(!verify_design_context_pin(
            &pin_a, &pack, "model-y", "rev-7"
        ));
        assert!(!verify_design_context_pin(
            &pin_a, &pack, "model-x", "rev-8"
        ));
    }
