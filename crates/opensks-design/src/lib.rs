/// Map a token path (`color.text.primary`, `size.hit_target.primary`) to a
/// camelCase Swift identifier (`colorTextPrimary`, `sizeHitTargetPrimary`).
pub fn swift_identifier(path: &str) -> String {
    let mut out = String::new();
    let mut capitalize_next = false;
    let mut started = false;
    for ch in path.chars() {
        if ch == '.' || ch == '_' || ch == '-' {
            capitalize_next = started;
            continue;
        }
        if capitalize_next {
            out.extend(ch.to_uppercase());
            capitalize_next = false;
        } else {
            out.push(ch);
        }
        started = true;
    }
    out
}

fn format_dimension(value: &serde_json::Value) -> String {
    if let Some(n) = value.as_i64() {
        return n.to_string();
    }
    if let Some(n) = value.as_u64() {
        return n.to_string();
    }
    if let Some(n) = value.as_f64() {
        if n.fract() == 0.0 {
            return (n as i64).to_string();
        }
        return n.to_string();
    }
    "0".to_string()
}

/// Compile a token set into a deterministic `GeneratedDesignTokens.swift`
/// source string. Aliases are resolved to concrete values first (see
/// [`compile_swift_tokens_checked`] for the strict, error-returning path); a set
/// that fails resolution falls back to its raw tokens so this infallible entry
/// point never panics. Output is stable for a given input (colors then
/// dimensions, each in token order), so the committed file is checked for drift.
pub fn compile_swift_tokens(set: &DesignTokenSet) -> String {
    match resolve_token_set(set) {
        Ok(resolved) => compile_swift_tokens_resolved(&resolved),
        Err(_) => compile_swift_tokens_resolved(&ResolvedTokenSet {
            design_system_id: set.design_system_id.clone(),
            revision: set.revision,
            tokens: set.tokens.clone(),
        }),
    }
}

/// Resolve aliases, then compile the resolved set into the Swift adapter.
/// Surfaces a typed [`DesignCompileError`] on a cyclic, unresolved, or
/// type-mismatched alias instead of silently degrading.
pub fn compile_swift_tokens_checked(set: &DesignTokenSet) -> Result<String, DesignCompileError> {
    let resolved = resolve_token_set(set)?;
    Ok(compile_swift_tokens_resolved(&resolved))
}

/// Compile a *resolved* token set into the deterministic Swift adapter. Token
/// order is preserved (colors in token order, then dimensions in token order),
/// so for an alias-free set this is byte-identical to the committed file.
pub fn compile_swift_tokens_resolved(set: &ResolvedTokenSet) -> String {
    let mut out = String::new();
    out.push_str("// GeneratedDesignTokens.swift\n");
    out.push_str("// GENERATED by `cargo run -p opensks-design --bin gen-swift-tokens`.\n");
    out.push_str(&format!(
        "// Source of truth: .opensks/design-systems/{}/tokens.json\n",
        set.design_system_id
    ));
    out.push_str(
        "// Do not edit by hand. The opensks-design drift test fails if this file is stale.\n\n",
    );
    out.push_str("import SwiftUI\n\n");
    out.push_str("public enum GeneratedDesignTokens {\n");
    out.push_str(&format!(
        "    public static let revision = {}\n",
        set.revision
    ));

    let colors: Vec<&DesignToken> = set
        .tokens
        .iter()
        .filter(|t| t.token_type == "color")
        .collect();
    if !colors.is_empty() {
        out.push_str("\n    // MARK: Colors\n");
        for token in colors {
            let ident = swift_identifier(&token.path);
            let hex = token
                .value
                .as_str()
                .unwrap_or_default()
                .trim_start_matches('#');
            out.push_str(&format!(
                "    public static let {ident} = Color(hex: \"{hex}\")\n"
            ));
        }
    }

    let dimensions: Vec<&DesignToken> = set
        .tokens
        .iter()
        .filter(|t| t.token_type == "dimension")
        .collect();
    if !dimensions.is_empty() {
        out.push_str("\n    // MARK: Dimensions\n");
        for token in dimensions {
            let ident = swift_identifier(&token.path);
            let number = format_dimension(&token.value);
            out.push_str(&format!(
                "    public static let {ident}: CGFloat = {number}\n"
            ));
        }
    }

    out.push_str("}\n");
    out
}

